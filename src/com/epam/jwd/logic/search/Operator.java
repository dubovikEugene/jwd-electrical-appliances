package com.epam.jwd.logic.search;

/**
 * Operators what can be used:
 * {@link #LESS_THAN}
 * {@link #EQUAL}
 * {@link #LARGER_THAN}
 */
public enum Operator {

    /**
     * operator less than something
     */
    LESS_THAN,

    /**
     * operator equal something
     */
    EQUAL,

    /**
     * operator larger than something
     */
    LARGER_THAN
}
